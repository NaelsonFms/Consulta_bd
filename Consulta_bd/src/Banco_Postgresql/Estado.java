package Banco_Postgresql;

public class Estado {
	
	private long id;
	private String nome;
	private String sigla;
	
	
	
	
	
	public long getId() {
		return this.id;
	}
	public void setId(long novo) {
		this.id = novo;
	}
	public String getNome() {
		return this.nome;
	}
	public void setNome(String novo) {
		this.nome = novo;
	}
	public String getSigla() {
		return this.sigla;
	}
	public void setSigla(String novo) {
		this.sigla = novo;
	}
	
	
	

}
